public class Link
{
    private String url;
    private int nestingLevel;
    private String urlParents;



    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

    public int getNestingLevel() {
        return nestingLevel;
    }

    public void setNestingLevel(int nestingLevel) {
        this.nestingLevel = nestingLevel;
    }

    public String getUrlParents() {
        return urlParents;
    }

    public void setUrlParents(String urlParents) {
        this.urlParents = urlParents;
    }


}
